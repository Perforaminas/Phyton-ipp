"""
Confecciona un programa que solicite al usuario (input) ingresar la temperatura en grados Celsisu y los convierta en grados Fahrenheit. Luego, se debe imprimir el resultado en pantalla.


- Confecciona un programa que solicite al usuario.
- Ingresar la temperatura en grados Celsius y la convierta en grados Fahrenheit.
- Luego, se debe imprimir el resultado en pantalla.

"""
Celsius = float(input("ingresa la temperatura en grados Celsius: "))

# convertir la temperatura de grados Fahrenheit

Fahrenheit = (Celsius * 9/5) + 32

# mostrar el resultado por pantalla 

print ("la temperatura en grados Fahrenheit es: ", Fahrenheit)